package ex.pkg04;
public class ConversaoDeUnidadeDeArea {
    public static double metroPes (double unidade){
        return unidade*10.76;
    }
    
    public static double pesCentimetros (double unidade){
        return unidade*929;
    }
    
    public static double centimetrosAcres (double unidade){
        return unidade*640;
    }
    
    public static double acrePes (double unidade){
        return unidade*43.560;
    }
}
